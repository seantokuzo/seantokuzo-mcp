const updatePRSchema = z.object({
  repository: repositoryField,
  pull_number: z
    .number()
    .int()
    .min(1)
    .optional()
    .describe(
      "The PR number to update. If omitted, auto-detected by finding the open PR for the current branch.",
    ),
  title: z.string().optional().describe("New title for the PR"),
  description: z
    .string()
    .optional()
    .describe("New description — or additional context when appending"),
  append_to_existing: z
    .boolean()
    .optional()
    .default(false)
    .describe(
      "If true, append the new description to the existing body instead of replacing it.",
    ),
});

const updatePullRequestTool = defineTool({
  name: "update_pull_request",
  description: `Update an existing pull request's title and/or description.

TIP: If the user doesn't specify a PR number, this tool auto-detects the PR for the current branch. Use this when the user wants to improve a PR description or update it after pushing new changes.`,
  inputSchema: updatePRSchema,
  handler: async (input, context) => {
    const client = getClient();

    const { repo, source: repoSource } = await resolveRepository(
      context,
      input.repository,
      client.defaultOwner,
    );
    const fullRepo = `${repo.owner}/${repo.repo}`;

    let pullNumber = input.pull_number;
    let prSource = "provided";
    if (!pullNumber) {
      const gitData = await getGitContextSafe(context);
      const branch = gitData?.currentBranch;
      if (!branch) {
        throw new Error(
          "No PR number specified and could not determine current branch.",
        );
      }
      const pr = await client.findPRForBranch(repo, branch);
      if (!pr) {
        throw new Error(
          `No open PR found for branch ${branch}. Please specify pull_number.`,
        );
      }
      pullNumber = pr.number;
      prSource = `auto-detected from branch ${branch}`;
    }

    const resolved = {
      repository: { value: fullRepo, source: repoSource },
      pullNumber: { value: pullNumber, source: prSource },
    };

    const currentPR = await client.getPullRequest(repo, pullNumber);

    // Body update logic:
    //   - append_to_existing=true + description given → append update section
    //     to currentPR.body (treating null body as empty string)
    //   - append_to_existing=true + no description → no body change
    //   - append_to_existing=false + description given → regenerate full body
    //     from commits + description (replace mode)
    //   - append_to_existing=false + no description → title-only update,
    //     leave body untouched so we don't wipe a hand-written description
    let newBody: string | undefined;
    const updateDescription = input.description?.trim() ?? "";

    if (input.append_to_existing) {
      if (updateDescription) {
        const existingBody = currentPR.body ?? "";
        newBody =
          existingBody +
          (existingBody ? "\n\n---\n\n" : "") +
          "## 📝 Update\n\n" +
          updateDescription;
      }
      // else: leave newBody undefined → Octokit won't touch the body
    } else if (updateDescription) {
      const commits = await client.getPRCommits(repo, pullNumber);
      newBody = generatePRDescription({
        commits,
        diffStats: {
          additions: currentPR.additions,
          deletions: currentPR.deletions,
          changedFiles: currentPR.changed_files,
        },
        userContext: input.description,
        sourceBranch: currentPR.head.ref,
        targetBranch: currentPR.base.ref,
      });
    }
    // else: title-only update path → newBody stays undefined

    const pr = await client.updatePullRequest({
      repo,
      pullNumber,
      title: input.title,
      body: newBody,
    });

    return {
      success: true,
      message: `✏️ Updated PR #${pr.number}`,
      resolved,
      data: {
        number: pr.number,
        title: pr.title,
        url: pr.html_url,
        repository: fullRepo,
        sourceBranch: currentPR.head.ref,
        targetBranch: currentPR.base.ref,
      },
    };
  },
});

