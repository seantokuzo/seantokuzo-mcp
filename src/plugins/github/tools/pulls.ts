const createPRSchema = z.object({
  repository: repositoryField,
  source_branch: z
    .string()
    .optional()
    .describe(
      "The branch with the changes. Auto-detected as the current branch if omitted.",
    ),
  target_branch: z
    .string()
    .optional()
    .describe(
      "The branch to merge into. Auto-detected as the default branch (main/master) if omitted.",
    ),
  title: z
    .string()
    .optional()
    .describe("PR title. If omitted, generated from the first commit message."),
  description: z
    .string()
    .optional()
    .describe(
      "The FULL PR description. When the user provides brief notes, expand them into a comprehensive description before calling this tool.",
    ),
  draft: z
    .boolean()
    .optional()
    .default(false)
    .describe("Whether to create as a draft PR"),
});

const createPullRequestTool = defineTool({
  name: "create_pull_request",
  description: `Create a new GitHub pull request.

TIP: Repo, source branch, and target branch are all auto-detected from the current git context if not provided. Call \`get_git_context\` first if you want to show the user what will be used.

AI-Enhanced Descriptions: When the user provides brief notes about their changes, YOU (Claude) should expand those into a comprehensive PR description BEFORE calling this tool. Include:
- A clear summary of what the PR does and why
- Key implementation details
- Breaking changes or migration notes
- Testing considerations

The 'description' parameter should contain the FULL description you generate — the tool will format it with headers and stats.`,
  inputSchema: createPRSchema,
  handler: async (input, context) => {
    const client = getClient();

    const { repo, source: repoSource } = await resolveRepository(
      context,
      input.repository,
      client.defaultOwner,
    );
    const fullRepo = `${repo.owner}/${repo.repo}`;

    const gitData = await getGitContextSafe(context);
    const sourceBranch = input.source_branch ?? gitData?.currentBranch ?? null;

    // Resolve target branch in priority order:
    //   1. Explicit input
    //   2. git-context defaultBranch (from local git remote HEAD)
    //   3. GitHub API repo.default_branch (authoritative — handles `master`
    //      and custom default branches for repos with no local clone)
    let targetBranch: string;
    let targetBranchSource: string;
    if (input.target_branch) {
      targetBranch = input.target_branch;
      targetBranchSource = "provided";
    } else if (gitData?.defaultBranch) {
      targetBranch = gitData.defaultBranch;
      targetBranchSource = "auto-detected (git default branch)";
    } else {
      const info = await client.getRepoInfo(repo);
      targetBranch = info.default_branch;
      targetBranchSource = "auto-detected (GitHub default_branch)";
    }

    const resolved = {
      repository: { value: fullRepo, source: repoSource },
      sourceBranch: {
        value: sourceBranch,
        source: input.source_branch
          ? "provided"
          : "auto-detected (current branch)",
      },
      targetBranch: {
        value: targetBranch,
        source: targetBranchSource,
      },
    };

    if (!sourceBranch) {
      throw new Error(
        "Could not determine source branch. Please specify source_branch.",
      );
    }
    if (sourceBranch === targetBranch) {
      throw new Error(
        `Source branch (${sourceBranch}) and target branch (${targetBranch}) are the same. Cannot create PR.`,
      );
    }

    const existingPR = await client.findPRForBranch(repo, sourceBranch);
    if (existingPR) {
      return {
        success: false,
        message: `A PR already exists for branch ${sourceBranch}`,
        resolved,
        data: {
          existingPR: {
            number: existingPR.number,
            title: existingPR.title,
            url: existingPR.html_url,
          },
        },
      };
    }

    // Commits/diff stats feed the PR description generator. If the compare
    // call fails (rare — usually transient or rate-limited), degrade
    // gracefully with a minimal description rather than failing the whole
    // PR creation. The client methods themselves throw so direct tool calls
    // (get_diff_stats, get_changed_files) still surface errors loudly.
    let commits: CommitInfo[] = [];
    let diffStats: DiffStats = { additions: 0, deletions: 0, changedFiles: 0 };
    try {
      commits = await client.getCommitsBetween(
        repo,
        targetBranch,
        sourceBranch,
      );
      diffStats = await client.getDiffStats(
        repo,
        targetBranch,
        sourceBranch,
      );
    } catch (err) {
      context.logger.warn(
        "Could not fetch commits/diff for PR description — creating with minimal description",
        err,
      );
    }

    const title =
      input.title ??
      commits[0]?.message.split("\n")[0] ??
      `Merge ${sourceBranch} into ${targetBranch}`;

    const body = generatePRDescription({
      commits,
      diffStats,
      userContext: input.description,
      sourceBranch,
      targetBranch,
    });

    const pr = await client.createPullRequest({
      repo,
      title,
      body,
      head: sourceBranch,
      base: targetBranch,
      draft: input.draft,
    });

    return {
      success: true,
      message: `🚀 Created PR #${pr.number}: ${pr.title}`,
      resolved,
      data: {
        number: pr.number,
        title: pr.title,
        url: pr.html_url,
        state: pr.state,
        draft: pr.draft,
        repository: fullRepo,
        sourceBranch,
        targetBranch,
        commits: commits.length,
        filesChanged: diffStats.changedFiles,
        additions: diffStats.additions,
        deletions: diffStats.deletions,
      },
    };
  },
});

