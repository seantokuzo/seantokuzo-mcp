const findPRSchema = z.object({
  repository: repositoryField,
  branch: z
    .string()
    .optional()
    .describe("The branch name to find a PR for. Auto-detected if omitted."),
});

const findPRForBranchTool = defineTool({
  name: "find_pr_for_branch",
  description:
    "Find the open pull request associated with a specific branch. Useful to check whether a PR already exists.",
  inputSchema: findPRSchema,
  handler: async (input, context) => {
    const client = getClient();

    const { repo, source: repoSource } = await resolveRepository(
      context,
      input.repository,
      client.defaultOwner,
    );
    const fullRepo = `${repo.owner}/${repo.repo}`;

    let branch = input.branch;
    let branchSource = "provided";
    if (!branch) {
      const gitData = await getGitContextSafe(context);
      branch = gitData?.currentBranch ?? undefined;
      branchSource = "auto-detected (current branch)";
    }

    if (!branch) {
      throw new Error(
        "No branch specified and could not determine current branch.",
      );
    }

    const resolved = {
      repository: { value: fullRepo, source: repoSource },
      branch: { value: branch, source: branchSource },
    };

    const pr = await client.findPRForBranch(repo, branch);

    if (!pr) {
      return {
        success: true,
        message: `No open PR found for branch ${branch}`,
        resolved,
        data: null,
      };
    }

    return {
      success: true,
      message: `Found PR #${pr.number} for branch ${branch}`,
      resolved,
      data: {
        number: pr.number,
        title: pr.title,
        url: pr.html_url,
        state: pr.state,
        repository: fullRepo,
      },
    };
  },
});

